package com.dqops.core.jobqueue.jobs.table;

import com.dqops.utils.exceptions.DqoRuntimeException;

/**
 * Exception thrown by {@link ImportTablesQueueJob} when importing tables failed.
 */
public class ImportTablesQueueJobException extends DqoRuntimeException {
    /**
     * Constructs a new runtime exception with the specified detail message.
     * The cause is not initialized, and may subsequently be initialized by a
     * call to {@link #initCause}.
     */
    public ImportTablesQueueJobException(String message) {
        super(message);
    }

    /**
     * Constructs a new runtime exception with the specified detail message and
     * cause.  <p>Note that the detail message associated with
     * {@code cause} is <i>not</i> automatically incorporated in
     * this runtime exception's detail message.
     */
    public ImportTablesQueueJobException(String message, Throwable cause) {
        super(message, cause);
    }
}
